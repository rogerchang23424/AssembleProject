	movzx ebx, level
	shl ebx, 2
	mov esi, OFFSET map_ptr	;將esi設定為地圖指標的頭
	add esi, ebx
	mov esi, dword ptr [esi] ;將esi設定為該關數地圖的指標，並放入堆疊
