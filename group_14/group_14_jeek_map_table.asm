	mov map_size, eax
	mov esi, OFFSET map_main_color_table	;設置牆壁顏色
	add esi, ebx
	mov eax, dword ptr [esi]
	mov map_main_color, eax
	pop esi
	
	;設定ecx
	movzx ecx, map_size.Y
	shl ecx, 4
	movzx eax, map_size.Y
	add ecx, eax
	;ecx = map_size.Y * map_size.X(map_size.X == 17)
	cld
	rep movsw

	add level, 1
	ret
LoadMap ENDP
